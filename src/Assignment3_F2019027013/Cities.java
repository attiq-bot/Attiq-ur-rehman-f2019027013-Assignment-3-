    public void fetchCitiesInComboBox() {
        CitiesList.setEnabled(false);
        try {
            Connection con = new DbConnection().getConnection();
            String query = "select city_name from Cities;";
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery(query);
            
            while(rs.next()){
                CitiesList.addItem(rs.getString("city_name"));
            }
            CitiesList.setEnabled(true);
            
        } catch (SQLException ex) {
            Logger.getLogger(Cities.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

  
